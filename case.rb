todays_weather = "foggy"

case todays_weather
    when "cloudy" then puts "There is a chance of meatballs, grab a bib."
    when "rainy" then puts "Tut tut, you'll need your black umbrella."
    when "sunny" then puts "Wear your NERD MACHINE tee."
    when "snowy" then puts "Make sure you bring TinTin along too."
    when "foggy" then puts "Wear blue, but expect a miracle."
    when "breezy" then puts "Don't wear a dress." 
else 
    puts "You're on your own with that one."  
end